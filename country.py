from countryinfo import CountryInfo
country = CountryInfo(input('Entrer le nom du Pays: '))

print('Nom du pays:', country.name())
print('Capitale:', country.capital())
print('Population:', country.population())
print('Superficie(en Km*):', country.area())
print('Region:', country.region())
print('Sous-Region:', country.subregion())
print('Gentile:', country.demonym())
print('Devise:', country.currencies())
print('Langues:', country.languages())
print('Frontieres:', country.borders())

